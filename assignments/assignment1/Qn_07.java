public class Qn_07 {
    public static void main(String[] args) {
        // == :- Relational operator that check the both
        // side value same or not
        // if same it returns true ,else return false....
        int a = 13;
        int b = 2;
        boolean result = a == b;
        System.out.println(result);

        // = :- assignment operator i.e assigns the value
        //right side value is evaluated first and assigned to left...
        // left side value will be displayed...

        System.out.println(a = b);
    }
}
